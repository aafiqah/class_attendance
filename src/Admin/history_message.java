    private void FileCombo()
    {
        try{
            String sql = "select * from historymessage";
            con = DriverManager.getConnection(serverurl+timezone,username,password);
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            contactnumber_drop.addItem("");
            while(rs.next())
            {
                //nolocalacc_drop.addItem("");
                contactnumber_drop.addItem(rs.getString("contact_number"));
            }
            
        }catch(SQLException e){
             JOptionPane.showMessageDialog(null,e);       
        }
    }
    
    public ArrayList<historymessageData> historymessage_list(){
        ArrayList<historymessageData> historymessage_list = new ArrayList<> ();
        String sql = "select* from historymessage";        
        
        try{
            con = DriverManager.getConnection(serverurl+timezone,username,password);
            smt = con.createStatement();
            rs = smt.executeQuery(sql);                
                       
            while(rs.next()){
                historymessageData historymessageData= new historymessageData();
                historymessageData.setId_history(rs.getString("id_message"));
                historymessageData.setContactnumber(rs.getString("contact_number"));
                historymessageData.setDate(rs.getString("datentime"));
                historymessageData.setMessage(rs.getString("message"));                      
                historymessage_list.add(historymessageData);                
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        return historymessage_list;        
    }
    
    public void setHistoryMessageReportTableData(){
        ArrayList<historymessageData> dataArray = historymessage_list();
        model = (DefaultTableModel) histroymessageData_table.getModel();
        model.setRowCount(0);
        Object[] rows = new Object[5];
        
        for(int i=0; i<dataArray.size(); i++)
        {
            rows[0] = dataArray.get(i).getId_history();
            rows[1] = dataArray.get(i).getContactnumber();
            rows[2] = dataArray.get(i).getDate();
            rows[3] = dataArray.get(i).getTime();
            rows[4] = dataArray.get(i).getMessage();
            model.addRow(rows);
        }
    }  
    
