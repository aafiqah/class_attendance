    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        String contact = contactnumber_txt.getText();
        String datentime = datentime_txt.getText();
        String message = message_txt.getText();

        try
        {
            String query ="Insert into historymessage (contact_number,datentime,message) values (?,?,?)";
            con = DriverManager.getConnection(serverurl+timezone,username,password);
            ps = con.prepareStatement(query);
            ps.setString(1, contact);
            ps.setString(2, datentime);
            ps.setString(3, message);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "New Data Add Successfully");
            setHistoryMessageReportTableData();
            resetData();
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_add_btnActionPerformed

    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        String contact = contactnumber_txt.getText();
        String datentime = datentime_txt.getText();
        String message = message_txt.getText();

        try
        {
            String sql="update historymessage set contact_number=?,datentime=?,message=? where id_message=?";
            con = DriverManager.getConnection(serverurl+timezone,username,password);
            ps = con.prepareStatement(sql);

            ps.setString(4, id_txt.getText());
            ps.setString(1, contact);
            ps.setString(2, datentime);  
            ps.setString(3, message);

            if (ps.executeUpdate() >0)
            {
                JOptionPane.showMessageDialog(null, "Update Data Successfully");
                setHistoryMessageReportTableData();
                resetData();
            }
        } catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this,"Cannot Update Data");
        }
    }//GEN-LAST:event_update_btnActionPerformed

