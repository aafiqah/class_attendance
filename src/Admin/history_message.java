    private void histroymessageData_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_histroymessageData_tableMouseClicked
        boolean a = histroymessageData_table.isEditing();
        if(a==false){
            JOptionPane.showMessageDialog(this, "Proceed to Edit page");
        }

        try
        {
            id =Integer.parseInt(histroymessageData_table.getValueAt(histroymessageData_table.getSelectedRow(), 0).toString());
            smt = con.createStatement();
            rs = smt.executeQuery("select * from historymessage where id_message=" + id);

            if (rs.next()) {

                id_txt.setText(rs.getString(1));
                contactnumber_txt.setText(rs.getString(2)); 
                datentime_txt.setText(rs.getString(3)); 
                message_txt.setText(rs.getString(4));
            }

            rs.close();
            smt.close();

        } catch( NumberFormatException | SQLException ex)
        {
            JOptionPane.showMessageDialog(this,"Cannot Procced to Edit Page");
        }
    }//GEN-LAST:event_histroymessageData_tableMouseClicked

